<h3>How to use this website</h3>

<p>
    We aim to educate people in no uncertain terms why and how people do certain drugs, and how it affects them.
    Our aim is to to increase the chance people are enjoying themselves if they're doing drugs recreationally, using what's best for them if they're using drugs medically, and safely if they're using them for any reason.

<p>
    There are 2 ways you can use this website (in its final form, which is beyond the scope of this class).
</p>

<p>
    1: Follow the "Education Track" which will be a archived weekly blog/podcast about the world of putting chemicals in your body, and alternatives to achieve the same or similar results.
</p>

<p>
    2: Search for the specific drug you're interested in to be taken to its description page with different dosages, use cases, risks, and non-chemical alternatives.
    Ideally have a search box here, perhaps through the Google plugin.
</p>



