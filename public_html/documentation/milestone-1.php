<h1> Do Drugs Well </h1>
<h2>Purpose</h2>

<p> This project will tell people in no uncertain terms why and how people do certain drugs, and how it affects them. </p>
<p> It will also gently, and stealthily, nudge people towards activities that achieve the same purpose without drug use. </p>

<h2>Audience</h2>

<p> This site is designed for people who either want to take drugs
    recreationally, but are not sure what effect it may have on them, how to do them, or what others use them for.

</p>

<h2>Goal</h2>
<p> Most information online today are either how drugs are bad for you, or personal and unprofessional "trip reports".
    Normalizing the conversation about chemicals we put into our bodies while referencing studies and their history,
    we aim to lessen the allure, mistique, and potential misuse. This could help many people that may
    over use, incorrect use, or benefit from certain chemicals. It worked for pot, it could work for everything else.
    Also references the podcast I would be recording about the same topic which in this imaginary world I'd have enough focus to shoot/record.

</p>

